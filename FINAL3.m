%{
某炼油厂将4种不同含硫量的液体原料(分别记为甲、乙、丙、丁)混合生产两种产品(分别记为A、B) 。
按照生产工艺的要求，原料甲、乙、丁必须首先倒入混合池中混合，混合后的液体再分别与原料丙混合
生产A、B。已知原料甲、乙、丙、丁的硫含量分别是3、1、2、1(%)，进货价格分别是6、16、10、15
(千元/吨)；产品A、B的含硫量分别不超过2.5、1.5(%)，售价分别是9、15(千元/吨)；根据市场信息，
原料甲、乙的供应没有限制，原料丙、丁的供应量最多为250、100吨，产品A、B市场需求量分别为300、500吨，
问应该怎样安排生产？
%}
% 定义成本系数
f = [6; 16; 10; 15]; % 进货价格

% 定义约束矩阵和约束向量
A = [-3 -1  0 -1;   % 产品A的硫含量约束
      0 -1 -2 -1;   % 产品B的硫含量约束
      0  0  1  0;   % 原料丙的供应量约束
      0  0  0  1;   % 原料丁的供应量约束
      1  1  0  0;   % 产品A的市场需求量约束
      0  0  1  1;   % 产品B的市场需求量约束
];

b = [
    -2.5 * (6+16+15); % 产品A的含硫量总量
    -1.5 * (10+15);   % 产品B的含硫量总量
    250;              % 原料丙的最大供应量
    100;              % 原料丁的最大供应量
    300;              % 产品A的市场需求量
    500               % 产品B的市场需求量
];

% 定义变量的上下界
lb = [0; 0; 0; 0];
ub = [];

% 求解线性规划问题
[x, fval] = linprog(f, A, b, [], [], lb, ub);

disp('生产安排:');
disp('原料甲的量:');
disp(x(1));
disp('原料乙的量:');
disp(x(2));
disp('原料丙的量:');
disp(x(3));
disp('原料丁的量:');
disp(x(4));
disp('总成本:');
disp(fval);
